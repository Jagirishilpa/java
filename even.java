class even
 {
  public static void main(String[] args)
   {
    int n=10;
    
    if(n>0)
     {
       if(n%2==0)
       { 
         System.out.println("given number is positive even");
       }
       else
       { 
        System.out.println("given number is even");
       }
     }
     else
       {
       if(n%2!=0)
       {
         System.out.println("given number is odd even");
       }
       else
        {
         System.out.println("given number is odd even");
        }
       }
  
   }

}